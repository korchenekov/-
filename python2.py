# Массив (список)
data = [5, 15, 25, 35, 45]

# Добавление элемента
data.append(55)

# Доступ к элементам
print(f"Элемент с индексом 2: {data[2]}")

# Размер массива
print(f"Размер массива: {len(data)}")

# Срезы
print(f"Срез [1:4]: {data[1:4]}")

# Стеки

# Использование списка как стека
stack = []

# Добавление элементов
stack.append("начало")
stack.append("середина")
stack.append("конец")

# Просмотр верхнего элемента
print(f"Верхний элемент стека: {stack[-1]}")

# Удаление элемента
stack.pop()

# Проверка на пустоту
print(f"Стек пустой? {'Да' if len(stack) == 0 else 'Нет'}")

# Размер стека
print(f"Размер стека: {len(stack)}")
